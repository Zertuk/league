function spellSelect(id) {
	switch(parseInt(id)) {
		default: return "N/A"
		break;
		case 6: return "Ghost"
		break;
		case 12: return "Teleport"
		break;
		case 14: return "Ignite"
		break;
		case 3: return "Exhaust"
		break;
		case 4: return "Flash"
		break;
		case 7: return "Heal"
		break;
		case 11: return "Smite"
		break;
		case 21: return "Barrier"
		break;
		case 10: return "Revive"
		break;

	}
}